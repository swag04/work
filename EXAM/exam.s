 AREA     appcode, CODE, READONLY
     EXPORT __main
	 IMPORT printMsg
	 IMPORT printMsg2p
	 IMPORT printMsg3p
     ENTRY 
__main  FUNCTION	
; IGNORE THIS PART
; Graphics code
; S18 & S17 CONTAIN X AND Y COORDINATE OF VGA SCREEN RESPECTIVELY
; S0 CONTAINS THETA VALUE AND S1 CONTAINS RADIUS VALUE
; sin(x) is stored in S12 
; cos(x) is stored in S11
; I HAVE TAKEN CENTER AS (20,20) AND RADIUS AS 20
		
		VLDR.F S0,= 0 ; THETA VALUE IS LOADED HERE IN RADIAN
		VLDR.F S1,= 20.0 ; RADIUS AND CENTER COORDINATES VALUE IS LOADED HERE		
		VLDR.F S15,= .1 ; CONSTANT VALUE USED FOR ADDITION
		VLDR.F S16,= 6.3 ; CONSTANT VALUE USED FOR COMPARISON
MAINLOOP
		LDR R3,= 0x00000016 ; N VALUE LOADED HERE
		VMOV.F S4,S0  ; THETA
		VLDR.F S5,= 1
		LDR R6,= 0x00000000 ;
		VLDR.F S6,= 1
		VLDR.F S7,= 1
		VLDR.F S10,= 1 ; CONSTANT VALUE USED FOR COMPARISON		
		VMOV.F S3,R3 ; N
		VCVT.F32.U32 S3,S3
		
		LDR R4,= 0x00000000 ; 
		LDR R10,= 0x00000000 ; 
		LDR R5,= 0x00000000 ; 
		LDR R0,= 0x00000001 ;
		VLDR.F S9,= 0 ; FINAL RESULT WILL BE STORED IN S9

; COS(X) IMPLEMENTATION STARTED HERE
COSLOOP
		LDR R12,= 0x00000000 ; R12 = 0 FOR COS FUNCTION
		CMP R10,R3
		BNE COSFUN
		VMOV.F S11,S9 ; FINAL COS(X) RESULT
		B SINELOOP

;SINE FUNCTION IMPLEMENTATION STARTED
SINELOOP		
		LDR R12,= 0x00000001 ; R12 = 1 FOR SINE FUNCTION
		LDR R4,= 0x00000000 ;
		LDR R10,= 0x00000000 ; 
		;LDR R11,= 0x00000000 ;
		BEQ SINEFUN
		
		
NEXTITRSINE
		
		CMP R10,R3 ;
		BNE SINEFUN
		VMOV.F S12,S9 ; FINAL SIN(X) RESULT
		VMUL.F S13,S12,S1 ; RSIN(THETA) = Y COORDINATE
		VMUL.F S14,S11,S1 ; RCOS(THETA) = X COORDINATE
		VADD.F S17,S13,S1 ; Y COORDINATE FOR VGA SCREEN
		VADD.F S18,S14,S1 ; X COORDINATE FOR VGA SCREEN
		
		;VCVT.U32.F32 S0,S0
		;VMOV R0,S0 ;
		
		VCVT.U32.F32 S17,S17
		VMOV R1,S17 ; 
		
		VCVT.U32.F32 S18,S18
		VMOV R0,S18 ;
		BL printMsg2p
		
		; THETA VALUE INCREAMENTED AND NEXT ITERATION START HERE
		VADD.F S0,S15
		VCMP.F S0,S16
		vmrs APSR_nzcv,FPSCR
		BLT MAINLOOP
		B stop
		
COSFUN
		B EVENODD ; TO DETECT n IS EVEN/ODD
		
; SIN(X) IS FOUND OUT IN THIS LOOP
SINEFUN
		AND R7,R4,#0x00000001
		ADD R4,R4,R4 
		ADD R4,#0x00000001
		CMP R4,#0x00000001
		LDR R5,= 0x00000000 ; 
		LDR R6,= 0x00000000 ;
		VLDR.F S5,= 1
		BEQ FIRSTITRSINE
		BNE POW1
; SERIES IS IMPLEMENTED ITERATIVELY HERE		
LOOP 
		VDIV.F S8,S0,S5
		CMP R7,R0
		ITE LT
		VADDLT.F S9,S9,S8
		VSUBGE.F S9,S9,S8 ; COS(X) RESULT IS STORED HERE
		ADD R10,#0x00000001
		MOV R4,R10
		VLDR.F S5,= 1
		VMOV.F S0,S4
		LDR R6,= 0x00000000 ;
		VLDR.F S6,= 1
		LDR R5,= 0x00000000 ; 
		CMP R12,#0x00000001
		BEQ NEXTITRSINE
		BNE COSLOOP 
		
; THIS LOOP IS USED TO FIND OUT X^2n OR X^(2n+1)
COSPOW 
		ADD R4,R4,R4
		CMP R4,#0x00000000
		BEQ FIRSTITR
		BNE POW1
POW1		

		SUB R11,R4,#0x00000001
		VMUL.F S0,S0,S4 ; S0 HAVE POWER VALUE
		ADD R5,#0x00000001
		CMP R5,R11
		BNE POW1
		BEQ FACT
		
FIRSTITR
		VMOV.F S0,S10
		VMOV.F S5,S10
		B LOOP
		
FIRSTITRSINE

		VMOV.F S0,S4
		VMOV.F S5,S4
		VMOV.F S9,S4
		ADD R10,#0x00000001
		mov r4,r10
		B NEXTITRSINE

; THIS LOOP IS USED TO FIND OUT FACTORIAL OF A NUMBER
FACT 
		CMP R4,#0x00000000
		BEQ FIRSTITR
		VMUL.F S5,S5,S6 ; S5 HAVE FACTORIAL RESULT
		;BGE COSLOOP
		ADD R6,#0x00000001
		VMOV.F S6,R6
		VCVT.F32.U32 S6,S6
		VADD.F S6,S6,S10
		CMP R6,R4
		BNE FACT
		BEQ LOOP
		
EVENODD
		AND R7,R4,#0x00000001
		B COSPOW
		
		
stop    B stop ; stop program
        ENDFUNC
        END 		
		